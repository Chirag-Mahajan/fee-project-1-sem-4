// WAP to show floating datatype in required format.
#include<stdio.h>
void main(){
    float a,b,c;
    printf("value of A is: ");
    scanf("%f",&a);
    printf("value of B is: ");
    scanf("%f",&b);
    printf("value of C is: ");
    scanf("%f",&c);
    printf("A=%f",a);
    printf("\nB=%f",b);
    printf("\nC=%f",c);
    return 0;

}